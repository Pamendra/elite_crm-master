class LeadDetails {
  final String id;
  final String name;
  final String cname;
  final String gmanager;
  final String pmanager;
  final String services;
  final String category;
  final String status;
  final String details;

  LeadDetails({
    required this.id,
    required this.name,
    required this.cname,
    required this.gmanager,
    required this.pmanager,
    required this.services,
    required this.category,
    required this.status,
    required this.details,
  });
}
